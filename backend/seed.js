import { initDB, dbRun, dbGet } from "./db.js";

async function seed() {
  await initDB();

  // Тестовый коллектив
  const existingCollective = await dbGet("SELECT id FROM collectives WHERE name = ?", ["Песнохорки"]);
  if (!existingCollective) {
    await dbRun(
      "INSERT INTO collectives (name, description, photo_url, age_range, schedule, teacher_name) VALUES (?, ?, ?, ?, ?, ?)",
      [
        "Песнохорки",
        "<p>Студия театра народной музыки «Песнохорки» — народный хор для детей и подростков 7–18 лет.</p><p>В программе: народный вокал, сценическое движение, нотная грамота. Регулярно участвуем в городских и всероссийских фестивалях. Есть адаптированные программы для детей с ОВЗ.</p>",
        null,
        "7–18 лет",
        "Пн, Ср, Пт 16:00",
        "Терентьева А.Ю.",
      ]
    );
    console.log("Создан коллектив: Песнохорки");
  }

  // Страница «О центре»
  const existingAbout = await dbGet("SELECT id FROM static_pages WHERE slug = ?", ["about"]);
  if (!existingAbout) {
    await dbRun(
      "INSERT INTO static_pages (slug, title, content, section) VALUES (?, ?, ?, ?)",
      [
        "about",
        "О центре",
        `<p>«Песнохорки» — центр эстетического воспитания, где дети развиваются через творчество и живую традицию. Мы знакомим ребят с культурой и историей своего народа не «по книжке», а через занятия, практику и выступления: ребёнок учится работать в команде, увереннее держится на сцене, развивает речь, внимание и дисциплину.</p>
<p>Центр работает с 10 апреля 1995 года и вырос из ансамбля «Песнохорки» при поддержке администрации и образования Индустриального района. Сегодня это многопрофильное учреждение, где объединения и направления собраны в единое воспитательно-образовательное этнографическое пространство.</p>
<p>На сайте вы можете посмотреть направления, выбрать объединение и педагога, увидеть награды, узнать про музей и подготовку к учебному году.</p>`,
        null,
      ]
    );
    console.log("Создана страница: О центре");
  }

  console.log("Seed завершён");
  process.exit(0);
}

seed().catch((err) => {
  console.error(err);
  process.exit(1);
});
